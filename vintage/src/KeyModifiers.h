#ifndef KEYMODIFIERS_H_
#define KEYMODIFIERS_H_

enum KeyModifiers
{
	KEYMOD_NONE  = 0x0000,
	KEYMOD_LSHIFT= 0x0001,
	KEYMOD_RSHIFT= 0x0002,
	KEYMOD_LCTRL = 0x0004,
	KEYMOD_RCTRL = 0x0008,
	KEYMOD_LALT  = 0x0010,
	KEYMOD_RALT  = 0x0020,
	KEYMOD_LMETA = 0x0040,
	KEYMOD_RMETA = 0x0080,
	KEYMOD_NUM   = 0x0100,
	KEYMOD_CAPS  = 0x0200,
	KEYMOD_MODE  = 0x0400
};

#endif
